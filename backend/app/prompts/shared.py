USCIS_EB1A_CRITERIA = """
EB-1A EXTRAORDINARY ABILITY CRITERIA (Updated October 2024):

1. AWARDS: Receipt of lesser nationally or internationally recognized prizes or awards for excellence in the field. (Oct 2024 update: team awards now qualify.)

2. MEMBERSHIPS: Membership in associations that require outstanding achievements of their members, as judged by recognized experts. (Oct 2024 update: past memberships count.)

3. PUBLISHED MATERIAL: Published material about the beneficiary in professional or major trade publications or other major media. (Oct 2024 update: no longer requires demonstrating "the value of the person's work.")

4. JUDGING: Participation as a judge of the work of others in the same or an allied field, either individually or on a panel.

5. ORIGINAL CONTRIBUTIONS: Evidence of original scientific, scholarly, artistic, athletic, or business-related contributions of major significance in the field.

6. SCHOLARLY ARTICLES: Authorship of scholarly articles in professional or major trade publications or other major media.

7. EXHIBITIONS: Evidence of display of work at artistic exhibitions or showcases (primarily for arts fields).

8. LEADING/CRITICAL ROLE: Performance in a leading or critical role for organizations or establishments that have a distinguished reputation.

9. HIGH SALARY: Evidence that the beneficiary has commanded a high salary or significantly high remuneration relative to others in the field.

10. COMMERCIAL SUCCESS: Evidence of commercial successes in the performing arts.

PRACTICAL NOTE: While USCIS requires meeting 3 of 10 criteria, declining approval rates in 2025-2026 mean practically meeting 4-5 criteria is advisable.
"""

USCIS_EB1B_CRITERIA = """
EB-1B OUTSTANDING PROFESSORS AND RESEARCHERS CRITERIA:

The petitioner must show the beneficiary is recognized internationally as outstanding in a specific academic area AND has at least 3 years of experience in teaching or research in that academic area AND has a permanent job offer for a tenure-track position, comparable research position, or comparable position at a private employer.

DOCUMENTARY EVIDENCE — must meet at least 2 of the following 6 criteria:

1. MAJOR PRIZES/AWARDS: Receipt of major prizes or awards for outstanding achievement in the academic field.

2. MEMBERSHIPS: Membership in associations in the academic field which require outstanding achievements of their members.

3. PUBLISHED MATERIAL BY OTHERS: Published material in professional publications written by others about the beneficiary's work in the academic field.

4. JUDGING: Evidence of the beneficiary's participation, either individually or on a panel, as the judge of the work of others in the same or an allied academic field.

5. ORIGINAL RESEARCH CONTRIBUTIONS: Evidence of the beneficiary's original scientific or scholarly research contributions to the academic field.

6. SCHOLARLY BOOKS/ARTICLES: Evidence of the beneficiary's authorship of scholarly books or articles (in scholarly journals with international circulation) in the academic field.

PRACTICAL NOTE: Must meet 2 of 6 documentary criteria. Unlike EB-1A, requires a permanent US job offer and 3 years of research/teaching experience. The "international recognition" standard is lower than EB-1A's "extraordinary ability" but still requires demonstrating standing above the majority of peers.
"""

USCIS_EB1C_CRITERIA = """
EB-1C MULTINATIONAL MANAGERS AND EXECUTIVES CRITERIA:

ALL of the following requirements must be met:

1. FOREIGN EMPLOYMENT: The beneficiary must have been employed outside the United States for at least 1 of the 3 years preceding the petition in a managerial or executive capacity by the qualifying organization.

2. MANAGERIAL/EXECUTIVE CAPACITY ABROAD: The beneficiary must have served in a managerial or executive capacity at the foreign entity. USCIS defines "managerial capacity" as managing an organization, department, subdivision, or function; supervising and controlling the work of other supervisory, professional, or managerial employees; having authority to hire/fire or recommend personnel actions; and exercising discretion over day-to-day operations. "Executive capacity" means directing management of the organization or a major component, establishing goals and policies, exercising wide latitude in discretionary decision-making, and receiving only general supervision from higher executives, board of directors, or stockholders.

3. US MANAGERIAL/EXECUTIVE ROLE: The beneficiary must be coming to the United States to serve in a managerial or executive capacity for the same organization, a parent, subsidiary, or affiliate.

4. QUALIFYING ORGANIZATIONAL RELATIONSHIP: There must be a qualifying relationship between the US and foreign entities — parent, subsidiary, affiliate, or branch. The same employer or its affiliate/subsidiary must exist in both countries.

5. US EMPLOYER DOING BUSINESS: The US employer must have been doing business for at least 1 year (if the US entity is new, it must have been doing business for at least 1 year at the time of filing).

PRACTICAL NOTE: All 5 requirements must be met. EB-1C does not require academic or research credentials — it is based on executive/managerial experience with a multinational organization. First-line supervisors generally do not qualify unless managing professionals.
"""

NIW_DHANASAR_FRAMEWORK = """
NIW (EB-2 NATIONAL INTEREST WAIVER) — DHANASAR FRAMEWORK (Matter of Dhanasar, 26 I&N Dec. 884, 2016):

The petitioner must demonstrate all three prongs:

1. SUBSTANTIAL MERIT AND NATIONAL IMPORTANCE: The proposed endeavor has both substantial merit and national importance. The endeavor must have broader implications beyond the individual's personal benefit. Fields like STEM, healthcare, education, and critical infrastructure are commonly accepted. The endeavor should be described in terms of its potential national impact, not just job duties.

2. WELL-POSITIONED TO ADVANCE: The beneficiary is well-positioned to advance the proposed endeavor. Evidence includes: education, skills, knowledge, and record of success in related or similar efforts. Publications, citations, patents, media coverage, and letters from experts in the field can demonstrate this. The key question is whether the beneficiary has the qualifications and track record to successfully advance the endeavor.

3. BENEFICIAL TO WAIVE REQUIREMENTS: On balance, it would be beneficial to the United States to waive the job offer and labor certification requirements. Consider whether: the person's contributions are of such value that the benefit of not requiring a specific job offer outweighs the national interest in the labor certification process; the person offers contributions of such value that even other qualified US workers cannot provide the same benefit; and requiring a specific job offer would adversely affect the proposed endeavor.

PRACTICAL NOTE: All 3 prongs must be satisfied. NIW requires an advanced degree (or equivalent) OR exceptional ability. Strong letters of recommendation from independent experts are particularly important. The "national importance" standard does not require that the impact be felt nationwide — regional or field-specific impact can suffice if it has broader implications.
"""

USCIS_O1A_CRITERIA = """
O-1A EXTRAORDINARY ABILITY CRITERIA (Sciences, Education, Business, Athletics):

Must demonstrate extraordinary ability by sustained national or international acclaim and meet at least 3 of the following 8 criteria:

1. AWARDS: Receipt of nationally or internationally recognized prizes or awards for excellence in the field of endeavor.

2. MEMBERSHIPS: Membership in associations in the field which require outstanding achievements of their members, as judged by recognized national or international experts.

3. PUBLISHED MATERIAL: Published material in professional or major trade publications or other major media about the beneficiary and the beneficiary's work in the field.

4. JUDGING: Participation on a panel, or individually, as a judge of the work of others in the same or in an allied field of specialization.

5. ORIGINAL CONTRIBUTIONS: Evidence of original scientific, scholarly, or business-related contributions of major significance in the field.

6. SCHOLARLY ARTICLES: Authorship of scholarly articles in the field, in professional journals, or other major media.

7. CRITICAL/ESSENTIAL CAPACITY: Evidence of employment in a critical or essential capacity for organizations and establishments that have a distinguished reputation.

8. HIGH REMUNERATION: Evidence that the beneficiary has either commanded a high salary or will command a high salary or other significantly high remuneration for services.

PRACTICAL NOTE: O-1A requires meeting 3 of 8 criteria. Unlike EB-1A (permanent residency), O-1A is a nonimmigrant (temporary) visa. O-1A can be a stepping stone to EB-1A — evidence used for O-1A approval strengthens a subsequent EB-1A petition. Criterion 7 (critical capacity) and Criterion 8 (high salary) are frequently used by tech workers.
"""

